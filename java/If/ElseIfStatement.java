package If;

public class ElseIfStatement {
    public static void main(String[] args) {
        int i = 80;
        int j = 60;
        if (i>70){
            System.out.println("I is big number");
        }else if (j>70){
            System.out.println("J is big number");
        }else {
            System.out.println("70 is Big Number");
        }
    }
}
